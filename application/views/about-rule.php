              <br><br>第一条　甲方采取的委托交易方式由双方书面约定。委托方式包括柜台委托及自助委托等，自助委托包括网上委托以及乙方认可的其他合法委托方式。甲方应充分了解各种委托系统的操作方法，乙方或乙方指定的代理机构对此有解答咨询的义务。
              <br><br>第二条　甲方通过本协议第十一条约定的委托交易方式下达的委托指令均以乙方或乙方指定的代理机构电脑记录资料为准。甲方进行柜台委托时，必须提供委托人（指甲方本人或其授权代理人，下同）身份证件和甲方账户卡，并在委托单上签字确认。如由代理人代为办理的，还须提供经公证的授权委托书。否则，乙方或乙方指定的代理机构有权拒绝受理甲方的委托，由此造成的后果由甲方承担。
