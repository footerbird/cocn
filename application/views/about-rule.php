          <p><b>六、网上委托</b>
              <br><br>第一条　网上委托是指乙方通过基于互联网的网上交易系统向甲方提供用于下达权益产品交易指令的一种服务方式。
              <br>网上交易的上网终端包括：电子计算机等设备。
              <br><br>第二条　网上委托方式除具有其他委托方式所共有的风险外，甲方还应充分了解和认识到其存在且不限于以下风险：
              <br>1、由于互联网络数据传输等原因，交易指令可能会出现中断、停顿、延迟、数据错误等情况；
              <br>2、甲方账号及密码信息泄露或客户身份可能被仿冒；
              <br>3、由于互联网上存在黑客恶意攻击的可能性，网络服务器可能会出现故障及其他不可预测的因素，行情信息及其他权益产品信息可能会出现错误或延迟；
              <br>4、甲方的网络终端设备及软件系统可能会受到非法攻击或病毒感染，导致无法下达委托或委托失败；
              <br>5、甲方的网络终端设备及软件系统与乙方所提供的网上交易系统不兼容，无法下达委托或委托失败；
              <br>6、如甲方缺乏网上委托经验，可能因操作不当造成委托失败或委托失误。
              <br>7、由于网络故障，甲方通过网上交易系统进行权益产品交易时，甲方网络终端设备已显示委托成功，而乙方服务器未接到其委托指令，从而存在甲方不能买入和卖出的风险；甲方网络终端设备对其委托未显示成功，于是甲方再次发出委托指令，而乙方服务器已收到投资者两次委托指令，并按其指令进行了转让过户，使甲方由此产生重复买卖的风险。
              <br>上述风险可能会导致甲方发生损失。
              <br><br>第三条　甲方可以通过网上委托获得浙江股权交易中心提供的其他委托方式所能够获得的相应服务。甲方办理网上委托后，只可操作浙江股权交易中心指定权益产品的委托。
              <br><br>第四条　甲方应当按照乙方的要求开通网上委托方式。甲方在使用乙方的网上交易客户端软件前，应完成乙方网上交易系统下载、安装。甲方不能扩散通过乙方网上委托系统获得的乙方提供的相关权益产品信息参考资料。
              <br><br>第五条　网上交易密码是网上权益产品交易的有效身份识别依据。
              <br>甲方应持本人有效身份证明文件、股东账户卡（IC卡）原件及其复印件以书面方式向乙方提出开通网上委托的申请，乙方应于受理当日或下个工作日为甲方开通网上委托。甲方应妥善保管本人的网上交易密码，不得擅自泄露、转交。网上交易密码如果泄露或遗忘，甲方有责任在第一时间到乙方或乙方指定的代理机构修改或重置。因甲方保管本人网上交易密码不善所带来的一切损失由甲方自行承担。
              <br><br>第六条　甲方进行网上委托所使用的软件必须是乙方或乙方指定的代理机构提供的或乙方指定站点下载的。甲方使用其他途径获得的软件，由此产生的后果由甲方自行承担。
              <br><br>第七条　凡使用甲方的网上交易密码、资金账号进行的网上委托均视为甲方亲自办理，由此所产生的一切后果由甲方承担。
              <br><br>第八条　甲方在办理网上委托的同时，应当开通柜台委托等其他委托方式，当乙方网上交易系统出现网络中断、高峰拥挤或网上委托被冻结等异常情况时，甲方可采用上述其他委托方式下达委托指令。
              <br><br>第九条　甲方通过网上委托的单笔委托及单个交易日最大成交金额按乙方的有关规定执行。
              <br><br>第十条　甲方在使用网上委托系统时，如果连续六次输错密码，乙方有权暂时冻结甲方的网上委托方式。连续输错密码的次数以乙方的电脑记录为准。甲方的网上委托被冻结后，如需当日解冻，甲方可以携带有效身份证件到乙方或乙方指定的代理机构申请解冻，乙方进行身份校验或审核无误后予以解冻。
              <br><br>第十一条　甲方应单独使用网上交易系统，不得与他人共享。甲方不得利用该网上交易系统从事权益产品代理买卖业务，并从中收取任何费用。
              <br><br>第十二条　当甲方有违反本协议第三十四条约定的情形时，乙方或乙方指定的代理机构有权冻结甲方的网上委托方式，并采取适当的方式追究甲方的法律责任。
              <br><br>第十三条　甲方通过互联网开通新的委托交易方式或新的业务品种时，应按照乙方指定的流程输入资金账号（或股东账号）、密码和其他身份校验信息。凡经乙方验证其输入无误的，即视其为甲方本人操作办理, 通过网上委托系统签署的申请表单或协议即视为甲方本人签署，由此所产生的一切后果由甲方承担。
              <br><br>第十四条　当本协议第二十五条列示的网上交易系统风险事项发生时，由此导致的甲方损失，乙方如无过错则不承担任何赔偿责任。本章未作约定的，适用本协议第三章的约定。
              <br><br>第十五条　发生下列情形之一，本协议终止：
              <br>一、甲乙双方的权益产品交易委托代理关系终止；
              <br>二、一方严重违反协议，另一方书面要求终止；
              <br>三、甲乙双方协商同意终止。
              <br>四、本协议有效期为一年，双方如无异议或变更，有效期自动顺延。
          </p>
          <p><b>七、变更终止</b>
              <br><br>第一条　当甲方重要资料变更时，应在发生变更后三个工作日内到乙方或乙方指定的代理机构按要求办理变更手续。
              <br>甲方重要资料包括但不限于机构名称、法定代表人、自然人姓名、有效证件号码、授权代理人或授权事项等。
              <br>当甲方一般资料变更时，应在发生变更后五个工作日内到乙方或乙方指定的代理机构按要求办理变更手续，或在网上委托系统自行变更（暂未开通）。
              <br>甲方一般资料包括但不限于证件地址、联系地址、联系电话。
              <br><br>第二条　除非甲方有未履行交易过户义务等违法违规情形或与乙方签订的各种协议未履行完毕的，甲方有权要求乙方或乙方指定的代理机构及时办理资金账户销户业务。
              <br><br>第三条　有下列情形之一的，乙方或乙方指定的代理机构可要求甲方限期纠正，甲方不能按期纠正或拒不纠正的，乙方或乙方指定的代理机构可视情形依法终止甲方的权益产品买卖资格或暂停甲方对其账户的使用：
              <br>1、乙方发现甲方向其提供的资料、证件严重失实；
              <br>2、乙方发现甲方的资金来源不合法；
              <br>3、甲方有严重损害乙方合法权益、影响其正常经营秩序的行为；
              <br>4、甲方违反交易规则，通过乙方的委托系统进行违法违规交易的；
              <br>5、法律、法规规定的其他情形。
              <br><br>第四条　乙方或乙方指定的代理机构终止甲方的权益产品买卖资格，需书面通知甲方，并说明理由。
