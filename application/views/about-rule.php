              <br>乙方或乙方指定的代理机构承担因其擅自泄露甲方资料给甲方造成的损失和法律后果。
              <br><br>第五条　甲方应妥善保管其身份证件、账户卡，因他人伪造身份证件、（变造）账户卡给甲方造成损失，除非乙方或乙方指定的代理机构有过错，否则应由侵权方承担相应法律责任，但乙方或乙方指定的代理机构有义务协助甲方向侵权方追偿。
              <br><br>第六条　当甲方遗失身份证件、账户卡时，应及时向乙方或乙方指定的代理机构办理挂失，乙方或乙方指定的代理机构只接受甲方的当面挂失，不接受其他任何形式的挂失请求，在挂失生效前已经发生的资金及权益产品的损失由甲方承担。
