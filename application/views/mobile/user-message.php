    <script type="text/javascript">
    $(function () {
      //左滑删除
      $(".delete-swipeout").each(function(){
          var msgH = $(this).parents(".weui-cell_swiped").height();
          $(this).css("line-height",(msgH-5)/100*2+"rem");
      })
      //单个删除
      $(document).on("click",".delete-swipeout",function(){
          $(this).parents(".weui-cell_swiped").remove();
          $.toast("已删除");
      })
      //批量删除
      $("#delBatch").on("click",function(){
          $.confirm("确定删除所选消息?", "消息管理", function() {
              $.toast("已删除");
          }, function() {
              //取消操作
          });
      })
      //点击编辑
      $("#editBatch").on("click",function(){
          $(".container").toggleClass('pb50');
          $("#quanxuan_bottom").toggle();
          $(".weui-cell__hd").toggle();
      })
      //全选
      $("#allSelect").on("click",function(){
		  if(this.checked){
			  $('#delBatch').removeClass('forbid');
		  }else{
			  $('#delBatch').addClass('forbid');
		  }
          $(".weui-cells_checkbox .weui-check").not("#allSelect").prop("checked",this.checked);
      })
      $(".weui-cells_checkbox .weui-check").not("#allSelect").on("click",function(){
          $("#allSelect").prop("checked",$(".weui-cells_checkbox .weui-check").not("#allSelect").length == $(".weui-cells_checkbox .weui-check:checked").not("#allSelect").length);
		  if($(".weui-cells_checkbox .weui-check:checked").not("#allSelect").length > 0){
			  $('#delBatch').removeClass('forbid');
		  }else{
			  $('#delBatch').addClass('forbid');
		  }
	  })
    })
    </script>
