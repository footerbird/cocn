    var chartsData = {};

    function drawCharts(index){//获取tab的索引号

        var myChart = echarts.init(document.getElementById("deal_tab_content"));

        var xAxisData = chartsData.date;
        var yAxisData = [];
        var axisTitle = "";
        switch(index){
            case 0:
                yAxisData = chartsData.ruzhu;
                axisTitle = '入驻公司';
                break;
            case 1:
                yAxisData = chartsData.faxing;
                axisTitle = '发行标数';
                break;
            case 2:
                yAxisData = chartsData.rongzi;
                axisTitle = '股权融资';
                break;
            case 3:
                yAxisData = chartsData.ruzhu;
                axisTitle = '合格投资人';
                break;
            default:
                yAxisData = chartsData.person;
                axisTitle = '入驻公司';
                break;
        }
        var option = {
            title: {
                show: true,
                text: axisTitle+'近10天增长趋势图',
                top: '18px',
                left: 'center',
                textStyle: {
                    fontSize: '12',
                    color: '#13426b',
                    fontWeight: 'normal',
                    fontFamily: 'PingFang SC'
                },
            },
            color: ['#67b8de'],
            grid: {
                left: '40px',
                right: '20px',
                bottom: '20px',
                top: '50px'
            },
            tooltip: {
                trigger: 'axis',
                axisPointer: {
                    type: 'cross',
                    label: {
                        backgroundColor: '#999'
                    }
                },
                formatter:function (params, ticket, callback) {
                    return "日期："+params[0].name+"<br/>数量："+params[0].value;
                },
                backgroundColor:'#58a2ef',
                textStyle: {
                    fontSize: 12
                }
            },
            toolbox: {
                feature: {
                    magicType : {
                        show: true,
                        type: ['bar'],
                        title: {
                            bar: '柱状图'
                        },
                        emphasis: {
                            iconStyle: {
                                borderColor: '#f4364c'
                            }
                        }
                    },
                    restore : {
                        show: true,
                        emphasis: {
                            iconStyle: {
                                borderColor: '#f4364c'
                            }
                        }
                    },
                    saveAsImage : {
                        show: false,
                        title: '保存',
                        emphasis: {
                            iconStyle: {
                                borderColor: '#f4364c'
                            }
                        }
                    }
                },
                top: 12,
                right: 15
            },
            xAxis: {
                type: 'category',
                data: xAxisData,
                axisLine: {
                    lineStyle: {
                        color: "#c6cdd2"
                    }
                },
                axisLabel: {
                    textStyle: {
                        color: "#546a79"
                    }
                }
            },
            yAxis: {
                type: 'value',
                name: '数量/个',
                nameTextStyle: {
                    color: '#13426b'
                },
                axisLine: {
                    lineStyle: {
                        color: "#c6cdd2"
                    }
                },
                axisLabel: {
                    textStyle: {
                        color: "#546a79"
                    }
                },
                splitLine : {
                    lineStyle: {
                        color: ['#e5e5e5'],
                        type: 'dashed'
                    }
                },
            },
            series: [{
                data: yAxisData,
                type: 'line',
                smooth: true,
                barMaxWidth: 10
            }]
        };

        myChart.setOption(option);

    }
    $(function(){
      $.ajax({
          type:"get",
