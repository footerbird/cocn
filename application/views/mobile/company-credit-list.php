    <script type="text/javascript">
    $(function () {
      //返回顶部
      $(".container").on("scroll",function(){
        	var $_top = $(".cont-box").offset().top;
        	var $_hh = $(".header").height();
        	var $_height = $(this).height();
          // console.log($_height);
        	if( $_top - $_hh + $_height < 0 ){
    			$("#back-top").fadeIn();
        	}else{
        		$("#back-top").fadeOut();
        	}
        });
        $(".ico-back-top").on("click",function(){//go top
        	$(".container").animate({scrollTop:0},500)
        });

      //加载更多
      var loading = false;  //状态标记
      $(document.body).infinite().on("infinite", function() {
        if(loading) return;
        loading = true;
        setTimeout(function() {
          $("#list").append("<li> 我是新加载的内容 </li>");
          loading = false;
        }, 1500);   //模拟延迟
      });

      //筛选
      $("#rightBar_choose .list li").find('.item').on('click',function(){
        if($(this).hasClass("choosed")) {
            $(this).removeClass("choosed");
        }else {
            $(this).addClass("choosed").siblings().removeClass("choosed");
        }
      });
      //重置
      $('#reset').on('click',function(){
        $("#rightBar_choose .list li").find('.item').removeClass("choosed");
      });
      })
    </script>
