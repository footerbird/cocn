              <h1 class="article-title">跑步“钱”进：中国资本是如何爆买美国生物医药资产的</h1>
              <div class="article-summary">
                <span>2019-09-09 19:09:09</span>
                <span class="fl-r">发布人：上下</span>
              </div>
              <section>
                  <p>“丰年好大雪，珍珠如土金如铁。”这说的可能就是今年美国生物医药初创企业面对“中国资本”的感受。</p><p>健康点发现，今年以来，中国风投和私募资金对美国生物医药产业的投资在多个领域均创下了历年来的记录。彭博新闻、英国《金融时报》、《华尔街日报》、路透社等多个全球主流财经媒体均关注并报道了中国资本对西方特别是美国生物医药初创公司的投资和购买热情。</p><p>其实中国资本的“买买买”热情从2017年就已经为人所关注。据《华尔街日报》报道，2017年，中国风投和私募对海外生物医疗类公司的投资达到了创纪录的35亿美元，而这一数字四年前仅为5亿美元。</p><p>英国《金融时报》认为，中国现在是美国生物技术初创公司最重要的资金来源之一。美国的生物医药初创公司去年募资大约100亿美元的风险投资资金，中国投资者参与了大约三分之一。</p><p>最早关注报道“中国钱”爆买现象的彭博新闻，今年四月份就发布了一篇报道。彭博新闻引用数据统计公司PitchBook的数据称，截至3月31日的2018年第一季度，总部位于中国的风险投资基金向美国私营生物技术公司注入了14亿美元，约占此类公司一季度37亿美元募资总额的40％。<br></p><p>健康点查阅了Pitchbook九月份的统计数据，发现今年迄今为止已经有42亿美元的投资流向美国生物医药公司。来自亚洲特别是中国的资金最多。</p><p>中国资本将投资热情转向海外首先可能与本土的生物医药公司的估值泡沫有关。</p><p>今年5月，腾盛博药宣布完成2.6亿美元早期融资，其联合创始人兼首席执行官洪志博士是全球传染病领域权威之一，曾在先灵葆雅、Valeant、GSK等跨国药企任职，在创办腾盛博药前是葛兰素史克抗感染疾病领域全球研发资深副总裁。</p><p>洪志告诉路透社，“有人告诉我，只要你自己创业，不会有任何融资难题。此前我还没有这种感觉，当我真正这样做的时候，整个过程让人难以置信。”今年5月，腾盛博药完成2.6亿美元早期融资。</p><p>启明创投负责医疗健康行业投资的主管合伙人梁颖宇在接受路透社采访时表示，有些中国公司甚至还没有开始临床试验就能获得超过4亿美元的投资条款，“我觉得这太疯狂了。”</p><p>在中国最近的本土生物医药企业的高估值泡沫下，投资人开始在美国寻求更好的回报。一些中国风险投资公司和资金管理公司已经成立了专门投资于美国和欧洲生命科学公司的基金。路透社列举了几个知名的基金，包括通和毓承、高瓴资本、蓝池资本（阿里高管成立的家族办公室，专门打理阿里巴巴高管们的个人资产）。</p><img class="" src="https://pic.36krcnd.com/201809/29024418/si83eg3wrfggu2of!heading"><p>今年初，通和毓承在短短一个月领投或参与了四个大手笔融资。包括对位于波士顿的肿瘤免疫疗法新锐TCR2 Therapeutics1.25亿美元的B轮融资，对Viela Bio的2.5亿美元A轮融资，对加州的肿瘤新药公司IDEAYA的9400万美元B轮融资，对UNITY5500万美元C轮融资。今年5月，跨境生命科技投资平台汇桥资本集团 (Ally Bridge Group) 宣布，完成对美国生物科技公司Grail3亿美元C轮融资的领投。</p><p>不少外媒认为，中国资本对生物医药行业的兴趣也得益于政府的鼓励。“中国制造 2025”将生物医药及高性能医疗器械作为十大重点领域之一。</p><p>希望能将先进技术引进中国国内是另一大原因。汇桥资本创始合伙人于凡就告诉《金融时报》：“在前沿创新，原创性和知识产权方面，美国公司通常具有明显的优势，”“我们将致力于把我们在海外投资的一些技术带回中国”。</p><p>路透社称，来自中国的现金流入已经夸大了美国生物技术公司的融资规模和筹资速度。类似腾盛博药或再鼎医药这样依赖许可协议拿到新药在中国开发和销售权益的公司 ，非常乐意支付溢价去购买海外管线资产。梁颖宇表示，“中国的兴趣使得美国药企的许可协议预付款从三年前的100万美元或500万美元增加到现在的3000多万美元。”</p><p>虽然外部环境对生物医药投资并不友好——比如美国特朗普政府对中国投资的限制愈发严格；港交所新政下上市的生物医药初创公司表现低迷——但中国生物技术投资依然逆风上升。全球知名投资人、奥博亚洲(OrbiMed Asia)资深董事总经理、创始合伙人王健认为，“对中国资本而言，寻找美国生物技术公司的投资机会很有意义，特别是因为许多中国生物技术公司在质量和产品管线方面仍然落后于美国同行。”</p><p>今年2月，跨国药企阿斯利康宣布，其全球生物制品研发部门MedImmune正通过将公司的6个早期炎症和自身免疫项目剥离出来，成立一家独立的生物技术公司Viela Bio。新公司将致力于依据疾病深层次原因开发用于严重自身免疫性疾病的治疗药物。</p><p>虽然阿斯利康依然是Viela最大的少数股东，但是Viela Bio获得的2.5亿美元的A轮融资中，通和毓承、博裕资本、高瓴资本组成的中国财团领衔了本轮投资。Viela的CEO姚正彬表示，有意入局的投资者提供的资金甚至超过了自己想要的筹资规模。</p><p>当然，中国资本的海外爆买之路也不是没有隐患。</p><p>首先当然是逐步升级的贸易战。未来美国政府会对来自中国的投资更加谨慎，这一谨慎是否会蔓延到生物医药领域尚未可知。今年8月美国通过的一项新法律扩大了美国外国投资委员会（CFIUS）的管辖范围，该委员会审查外国投资是否存在潜在的国家安全问题。 未来这一委员会要在更广泛的范围优先保护美国的新兴技术，这其中就包括遗传信息等生物信息技术。</p><p>其次，希望能将美国技术引进国内的愿望也很难完全如愿。路透社的报道虽然肯定了中国药监部门近年的改革，但依然表示，“中国市场依然难以驾驭。 在中国开展新药临床试验、获得政府批准进而销售药品都存在巨大的障碍。”</p><p>而且美国的从业者也未必都对中国资本双手欢迎。《华尔街日报》称，大量涌入的中国钱甚至让美国的从业者都开始担忧：即担心这会助长美国市场的泡沫，也担心“来自中国的缺乏经验的投资者会低估投资风险并在经济低迷时逃离。”</p><p>参考资料：</p><p>1.Chinese Money Floods U.S. Biotech as Beijing Chases New Cures，bloomberg；</p><p>2.As China builds biotech sector, cash floods U.S. startups，Reuters；</p><p>3.China’s Investors Pour Into Western Biotech Startups，WSJ；</p>
